export default function RealEstateApp() {
  const [account, setAccount] = useState(null);
  const [contract, setContract] = useState(null);
  const [properties, setProperties] = useState([]);
  const [location, setLocation] = useState("");
  const [price, setPrice] = useState("");

  useEffect(() => {
    const init = async () => {
        if (window.ethereum) {
            const provider = new ethers.BrowserProvider(window.ethereum);
            const signer = await provider.getSigner();
            // Directly use the contract address without ENS resolution
            setContract(new ethers.Contract(contractAddress, abi, signer));
        }
    };
    init();
  }, []);

  const connectWallet = async () => {
    if (window.ethereum) {
      const accounts = await window.ethereum.request({ method: "eth_requestAccounts" });
      setAccount(accounts[0]);
    } else {
      alert("MetaMask is not installed!");
    }
  };

  const listProperty = async () => {
    try {
        if (contract && account) {
            const tx = await contract.listProperty(location, ethers.parseEther(price));  // Changed from ethers.utils.parseEther
            await tx.wait();
            alert("Property listed successfully!");
        }
    } catch (error) {
        console.error("Error listing property:", error);
        alert("Error listing property. See console for details.");
    }
};

const buyProperty = async (id, price) => {
    try {
        if (contract && account) {
            const tx = await contract.buyProperty(id, { value: ethers.parseEther(price) });  // Changed from ethers.utils.parseEther
            await tx.wait();
            alert("Property purchased successfully!");
        }
    } catch (error) {
        console.error("Error buying property:", error);
        alert("Error buying property. See console for details.");
    }
};

  return (
    <div className="p-6 space-y-4">
      <h1 className="text-2xl font-bold">Real Estate Tokenization</h1>
      <Button onClick={connectWallet}>{account ? `Connected: ${account}` : "Connect Wallet"}</Button>
      
      <div className="space-y-2">
        <Input placeholder="Property Location" value={location} onChange={(e) => setLocation(e.target.value)} />
        <Input placeholder="Price in ETH" value={price} onChange={(e) => setPrice(e.target.value)} />
        <Button onClick={listProperty}>List Property</Button>
      </div>
      
      <div className="grid grid-cols-1 md:grid-cols-3 gap-4">
        {properties.map((prop, index) => (
          <Card key={index}>
            <CardContent>
              <p><strong>Location:</strong> {prop.location}</p>
              <p><strong>Price:</strong> {prop.price} ETH</p>
              <Button onClick={() => buyProperty(prop.id, prop.price)}>Buy</Button>
            </CardContent>
          </Card>
        ))}
      </div>
    </div>
  );
}
